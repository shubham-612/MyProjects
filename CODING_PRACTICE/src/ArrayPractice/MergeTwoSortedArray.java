package ArrayPractice;
import java.util.HashSet;

public class MergeTwoSortedArray {

	public static void main(String[] args) {
		int []arr= {1,3,5};
		int []arr1= {2,4,6};
		HashSet<Integer>h=new HashSet<>();
		int i=0,j=0,k=0;
		while(i!=arr.length)
		{
			h.add(arr[i]);
			i++;
			k++;
		}
		while(j!=arr.length)
		{
			h.add(arr1[j]);
			j++;
			k++;
		}
		System.out.println(h);

	}

}
